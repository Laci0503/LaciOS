uint8 readConfusingRegister(uint8 index){
    inb(0x3da);
    outb(0x3c0,index);
    return inb(0x3c1);
}

void writeConfusingRegister(uint8 index, uint8 data){
    inb(0x3da);
    outb(0x3c0,index);
    outb(0x3c0,data);
}

uint8 readMiscPort(){
    return inb(0x3cc);
}
void writeMiscPort(uint8 data){
    outb(0x3c2,data);
    return;
}

uint8 readIndexed(uint8 port, uint8 index){
    switch (port)
    {
    case 0:
        outb(0x3c4,index);
        return inb(0x3c5);
    case 1:
        outb(0x3ce,index);
        return inb(0x3cf);
    case 2:
        outb(0x3d4,index);
        return inb(0x3d5);
    }
    return 0;
}
void writeIndexed(uint8 port, uint8 index, uint8 data){
    switch (port)
    {
    case 0:
        outb(0x3c4,index);
        outb(0x3c5,data);
        return;
    case 1:
        outb(0x3ce,index);
        outb(0x3cf,data);
        return;
    case 2:
        outb(0x3d4,index);
        outb(0x3d5,data);
        return;
    }
    return;
}

void setChain4(uint8 enabled){
    if(enabled){
        writeIndexed(0,0x4,readIndexed(0,0x4) | 0b00001000);
    }else{
        writeIndexed(0,0x4,readIndexed(0,0x4) & 0b11110111);
    }
    return;
}

void videoLoadSettings(uint8* settings){
    uint8* pointer = settings;
    writeMiscPort(*pointer);
    pointer++;
    for(uint8 i=0;i<5;i++){
        writeIndexed(0,i,*pointer);
        pointer++;
    }
    writeIndexed(2,0x3,readIndexed(2,0x3) | 0x80);
    writeIndexed(2,0x11,readIndexed(2,0x11) & ~0x80);

    pointer[0x3] |= 0x80;
    pointer[0x11] &= ~0x80;

    for(uint8 i=0;i<25;i++){
        writeIndexed(2,i,*pointer);
        pointer++;
    }

    for(uint8 i=0; i<9;i++){
        writeIndexed(1,i,*pointer);
        pointer++;
    }

    for(uint8 i=0;i<21;i++){
        writeConfusingRegister(i,*pointer);
        pointer++;
    }
    inb(0x3da);
    outb(0x3c0,0x20);
}

void set_plane(uint8 p)
{
	uint8 pmask;
	p &= 3;
	pmask = 1 << p;

    writeIndexed(1,4,p);
    writeIndexed(0,2,pmask);
}

