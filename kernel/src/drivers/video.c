void nextLine(){
    if (vga_index/WIDTH==24){
        for(uint8 i=0;i<HEIGHT-1;i++){
            for(uint8 j=0;j<WIDTH;j++){
                TERMINAL_BUFFER[i*WIDTH+j]=TERMINAL_BUFFER[(i+1)*WIDTH+j];
            }
        }
        for(uint8 i=0;i<WIDTH;i++){
            TERMINAL_BUFFER[(HEIGHT-1)*WIDTH+i]=0;
        }
        vga_index=(HEIGHT-1)*WIDTH;
    }else{
        vga_index=((vga_index/WIDTH)+1)*WIDTH;
    }
    return;
}

void write_letter(char letter, uint8 fore_color, uint8 back_color){
    if (vga_index==WIDTH*HEIGHT-1){
        nextLine();
    }
	if(letter=='\n'){
		nextLine();
		return;
	}
    TERMINAL_BUFFER[vga_index]=((back_color<<4 | fore_color) << 8) | letter;
    vga_index++;
    return;
}
void write_text(char text[], uint8 fore_color, uint8 back_color){
    uint16 l=len(text);
    for(uint16 i=0; i<l;i++){
        if (((vga_index+1)%WIDTH)==0){nextLine();}
        write_letter(text[i],fore_color,back_color);
    }
    return;
}

void print(char text[]){
    write_text(text,forecolor,backcolor);
}

void clear(){
    for(uint16 i=0;i<WIDTH*HEIGHT;i++){
        TERMINAL_BUFFER[i]=0;
    }
    vga_index=0;
    return;
}

